from smtplib import SMTP, SMTP_SSL
from email.mime.text import MIMEText

config = require('./config')


def make_smtp():
  if str(config['registry.email.smtp_ssl']).lower() == 'true':
    cls = SMTP_SSL
  else:
    cls = SMTP
  return cls(config['registry.email.smtp_host'])
